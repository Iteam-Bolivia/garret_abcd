  <p><strong>QUIENES SOMOS?</strong></p>
  <p>Somos una Biblioteca particular, sin fines de lucro que tiene el objetivo de socializar y difundir su acervo documental. El mismo ha sido formado en el transcurso de m&aacute;s de cincuenta a&ntilde;os  debido a la necesidad de informaci&oacute;n de su propietario Carlos Garrett Zamora, muy ligado a la investigaci&oacute;n y an&aacute;lisis de la historia de Bolivia, la econom&iacute;a, la industria minera, la tecnolog&iacute;a, las relaciones laborales, el derecho, la sociolog&iacute;a, la pol&iacute;tica, etc. Ha dedicado su tiempo, energ&iacute;a y recursos para  posibilitar que este objetivo se haga realidad.</p>
